# ---------------------------------------------------------------------------
# Repo root resolution (mirrors readme_gen._resolve_invoking_repo_root)
# ---------------------------------------------------------------------------

def _resolve_repo_root() -> Path:
    """Resolve the repo root from the invoking worktree's cwd."""
    import subprocess
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--show-toplevel"],
            capture_output=True, text=True, timeout=5, cwd=os.getcwd(),
        )
        if result.returncode == 0:
            root = Path(result.stdout.strip())
            if root.is_dir():
                return root
    except Exception:
        pass
    # Fallback: script-file parent's parent
    return Path(__file__).resolve().parent.parent


REPO_ROOT = _resolve_repo_root()
DECISIONS_DIR = REPO_ROOT / "decisions"
RULES_DIR = REPO_ROOT / ".claude" / "rules"
GENERATED_HEADER = (
    "<!-- GENERATED by tools/gen_rules.py — DO NOT EDIT — regenerate to update -->\n"
)

# ---------------------------------------------------------------------------
# Frontmatter parser (stdlib, no PyYAML)
# ---------------------------------------------------------------------------

def _parse_frontmatter(text: str) -> dict | None:
    """
    Parse YAML frontmatter from the top of an ADR file.

    Returns a dict of key→value if frontmatter is present, else None.
    Only supports the subset of YAML used in ADR frontmatter:
      - scalar strings (quoted or unquoted)
      - empty lists: []
      - inline list items starting with "  - " lines
    """
    lines = text.splitlines()
    if not lines or lines[0].strip() != "---":
        return None

    # Find the closing ---
    close_idx = None
    for i, line in enumerate(lines[1:], start=1):
        if line.strip() == "---":
            close_idx = i
            break
    if close_idx is None:
        return None

    fm_lines = lines[1:close_idx]
    result: dict = {}
    current_key: str | None = None

    for line in fm_lines:
        # List item continuation
        if line.startswith("  - ") and current_key is not None:
            item = line[4:].strip().strip('"').strip("'")
            if not isinstance(result.get(current_key), list):
                result[current_key] = []
            result[current_key].append(item)
            continue

        # Empty list shorthand: key: []
        m = re.match(r'^(\w+):\s*\[\s*\]\s*$', line)
        if m:
            current_key = m.group(1)
            result[current_key] = []
            continue

        # Scalar: key: value
        m = re.match(r'^(\w+):\s*(.*)', line)
        if m:
            current_key = m.group(1)
            val = m.group(2).strip().strip('"').strip("'")
            result[current_key] = val
            continue

    return result


# ---------------------------------------------------------------------------
# ADR discovery
# ---------------------------------------------------------------------------

def _load_adrs() -> list[dict]:
    """
    Load all ADRs in decisions/ that have frontmatter.

    Returns list of dicts with keys:
      path, id, status, supersedes, superseded_by, scope, rule_ids
    """
    adrs = []
    for adr_path in sorted(DECISIONS_DIR.glob("[0-9]*.md")):
        try:
            text = adr_path.read_text(encoding="utf-8", errors="replace")
        except OSError:
            continue

        fm = _parse_frontmatter(text)
        if fm is None:
            # No frontmatter — skip gracefully
            continue

        # Normalize required keys
        adr_id = str(fm.get("id", "")).strip()
        status = str(fm.get("status", "")).strip().lower()
        supersedes = fm.get("supersedes", [])
        if isinstance(supersedes, str):
            supersedes = [supersedes] if supersedes else []
        superseded_by = fm.get("superseded_by", [])
        if isinstance(superseded_by, str):
            superseded_by = [superseded_by] if superseded_by else []
        scope = str(fm.get("scope", "")).strip()
        rule_ids = fm.get("rule_ids", [])
        if isinstance(rule_ids, str):
            rule_ids = [rule_ids] if rule_ids else []

        if not adr_id or not scope or not rule_ids:
            # Incomplete frontmatter — skip
            continue

        adrs.append({
            "path": adr_path,
            "id": adr_id,
            "status": status,
            "supersedes": supersedes,
            "superseded_by": superseded_by,
            "scope": scope,
            "rule_ids": rule_ids,
        })

    return adrs


def _is_superseded(adr: dict) -> bool:
    """Return True if this ADR is superseded (should be excluded from digest)."""
    if adr["status"] == "superseded":
        return True
    if adr.get("superseded_by"):
        return True
    return False


