#!/usr/bin/env python3
"""
tools/gen_rules.py — generate .claude/rules/<scope>.md from ADR frontmatter.

Mirrors the dashboard/server.py --generate-readme pattern (stdlib-only, no
third-party YAML parser needed — frontmatter is simple key: value syntax).

Rule ID scheme: <SCOPE3>-NNN where SCOPE3 is the first 3 characters of the
scope name uppercased (e.g. scope "capture" → "CAP"), and NNN is a zero-padded
counter within that scope.  Rule IDs are declared in ADR frontmatter
``rule_ids`` lists and are STABLE — never reused, never reordered.

ADR Frontmatter keys (required for inclusion):
  id           — ADR identifier (e.g. "ADR-0006")
  status       — "accepted" | "superseded" (case-insensitive)
  supersedes   — list of ADR IDs this supersedes (may be empty list)
  superseded_by — list of ADR IDs that supersede this (may be empty list)
  scope        — short slug (e.g. "capture", "commits", "pipeline")
  rule_ids     — list of stable rule_ids (e.g. ["CAP-001", "CAP-002"])

ADRs with status "superseded" (or any superseded_by entry) are excluded from
the generated output.  ADRs without frontmatter are skipped gracefully.

Output: .claude/rules/<scope>.md per scope found in non-superseded ADRs.
Header: <!-- GENERATED by tools/gen_rules.py — DO NOT EDIT — regenerate to update -->

Usage:
  python tools/gen_rules.py          # writes all discovered scopes
  python tools/gen_rules.py --check  # dry-run: exits non-zero if output differs
"""

import os
