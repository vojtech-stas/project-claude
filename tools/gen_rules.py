def _render_scope_rules(scope: str, adrs: list[dict]) -> str:
    """
    Render the rule-list body for the given scope + ADRs.

    This is shared between the AREA file renderer and the CLAUDE.md
    region renderer.  Returns the rule-list lines only (no file header,
    no paths: frontmatter — callers add those).
    """
    lines: list[str] = []
    lines.append(f"### {scope.capitalize()} rules\n")
    lines.append(
        f"Atomic rules for the `{scope}` scope, generated from non-superseded ADR "
        f"frontmatter by `tools/gen_rules.py`.\n"
    )
    for adr in adrs:
        adr_id = adr["id"]
        adr_file = adr["path"].name
        lines.append(f"\n#### Source: {adr_id} (`decisions/{adr_file}`)\n")
        for rule_id in adr["rule_ids"]:
            statement = _get_rule_statement(rule_id, adr_id)
            lines.append(f"- **{rule_id}:** {statement}\n")
    return "".join(lines)


def _render_scope(scope: str, adrs: list[dict]) -> str:
    """
    Render .claude/rules/<scope>.md content for an AREA scope.

    AREA files include:
      - The generated-file header comment
      - paths: YAML frontmatter (so Claude Code loads only when relevant)
      - The rule-list body
    """
    paths_val = SCOPE_PATHS.get(scope, "")
    lines: list[str] = []
    lines.append(GENERATED_HEADER)
    # paths: frontmatter line (ADR-0073 D1 — area scopes must declare paths:)
    if paths_val:
        lines.append(f"paths: {paths_val}\n")
    lines.append("\n")
    lines.append(f"# {scope.capitalize()} rules\n")
    lines.append(
        f"Atomic rules for the `{scope}` scope, generated from non-superseded ADR "
        f"frontmatter by `tools/gen_rules.py`.\n"
    )
    for adr in adrs:
        adr_id = adr["id"]
        adr_file = adr["path"].name
        lines.append(f"\n## Source: {adr_id} (`decisions/{adr_file}`)\n")
        for rule_id in adr["rule_ids"]:
            statement = _get_rule_statement(rule_id, adr_id)
            lines.append(f"- **{rule_id}:** {statement}\n")
    return "".join(lines)


def _render_claude_md_region(scope: str, adrs: list[dict]) -> str:
    """
    Render the full CLAUDE.md generated-region block for a GLOBAL scope.

    The block is bounded by:
      <!-- BEGIN GENERATED:rules:<scope> -->
      ...content...
      <!-- END GENERATED:rules:<scope> -->

    This is the content that replaces the region between the markers in
    CLAUDE.md (ADR-0073 D1 — generated-region path).
    """
    begin = CLAUDE_MD_BEGIN_MARKER.format(scope=scope)
    end = CLAUDE_MD_END_MARKER.format(scope=scope)
    body = _render_scope_rules(scope, adrs)
    return f"{begin}\n{body}{end}\n"


def _update_claude_md_region(scope: str, new_region: str) -> tuple[str, bool]:
    """
    Replace the generated region for `scope` in CLAUDE.md.

    Returns (new_content, changed) where changed=True if the region
    was actually replaced (or newly inserted).

    If the markers don't exist yet, the region is APPENDED to CLAUDE.md
    (bootstrap for first-time insertion).
    """
    begin = CLAUDE_MD_BEGIN_MARKER.format(scope=scope)
    end = CLAUDE_MD_END_MARKER.format(scope=scope)

    try:
        existing = CLAUDE_MD.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return "", False

    # Find the marker pair
    begin_pos = existing.find(begin)
    end_pos = existing.find(end)

    if begin_pos != -1 and end_pos != -1 and end_pos > begin_pos:
        # Replace the entire block (markers + content between them)
        after_end = existing[end_pos + len(end):]
        new_content = existing[:begin_pos] + new_region + after_end
        changed = new_content != existing
        return new_content, changed
    else:
        # Markers not found — append the region (first-time bootstrap)
        new_content = existing.rstrip("\n") + "\n\n" + new_region + "\n"
        return new_content, True


