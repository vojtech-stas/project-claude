def _count_rule_ids(scopes: dict[str, list[dict]]) -> int:
    """
    Count the total number of rule_ids across all active scopes.

    Used by --check to report/verify conservation of rule_ids across
    relocation between CLAUDE.md and .claude/rules/ (ADR-0073 D5).
    """
    total = 0
    for scope_adrs in scopes.values():
        for adr in scope_adrs:
            total += len(adr.get("rule_ids", []))
    return total


def generate(check_mode: bool = False) -> int:
    """
    Generate (or diff-check) scope outputs per SCOPE_TARGET classification.

    GLOBAL scopes → CLAUDE.md generated-region blocks (between markers).
    AREA scopes   → .claude/rules/<scope>.md (with paths: frontmatter).

    Args:
        check_mode: if True, compare fresh output to committed state and
                    exit non-zero if any differ (mirrors CHECK 2 behavior).
                    Also prints the total rule_id count for conservation checks.

    Returns exit code (0 = success, 1 = failure/dirty).
    """
    adrs = _load_adrs()

    # Separate into active (non-superseded) and superseded
    active_adrs = [a for a in adrs if not _is_superseded(a)]

    if not active_adrs:
        print("gen_rules.py: no active ADRs with frontmatter found.", file=sys.stderr)
        return 0

    # Group by scope
    scopes: dict[str, list[dict]] = {}
    for adr in active_adrs:
        scopes.setdefault(adr["scope"], []).append(adr)

    # Count rule_ids (conservation check — ADR-0073 D5)
    total_rule_ids = _count_rule_ids(scopes)
    print(f"gen_rules.py: total rule_ids across all active scopes: {total_rule_ids}")

    # Ensure .claude/rules/ exists (unless check_mode)
    if not check_mode:
        RULES_DIR.mkdir(parents=True, exist_ok=True)

    any_dirty = False
    generated_files: list[str] = []

    for scope, scope_adrs in sorted(scopes.items()):
        target = SCOPE_TARGET.get(scope, "area")  # default AREA for unknown scopes

        if target == "global":
            # GLOBAL scope → render into CLAUDE.md generated-region block
            new_region = _render_claude_md_region(scope, scope_adrs)

            if check_mode:
                # Verify CLAUDE.md contains the expected region
                try:
                    current = CLAUDE_MD.read_text(encoding="utf-8", errors="replace")
                except OSError:
                    print(
                        f"DIRTY: CLAUDE.md not found "
                        f"(run 'python tools/gen_rules.py' to generate regions)",
                        file=sys.stderr,
                    )
                    any_dirty = True
                    continue

                begin = CLAUDE_MD_BEGIN_MARKER.format(scope=scope)
                end = CLAUDE_MD_END_MARKER.format(scope=scope)
                begin_pos = current.find(begin)
                end_pos = current.find(end)

                if begin_pos == -1 or end_pos == -1:
                    print(
                        f"DIRTY: CLAUDE.md missing generated-region markers for "
                        f"scope '{scope}' "
                        f"(run 'python tools/gen_rules.py' and commit)",
                        file=sys.stderr,
                    )
                    any_dirty = True
                else:
                    # Extract existing region (markers + content)
                    after_end = current[end_pos + len(end):]
                    existing_region = current[begin_pos:end_pos + len(end)]
                    # Compare without trailing newline to be robust
                    expected = new_region.rstrip("\n")
                    actual = existing_region.rstrip("\n")
                    if actual != expected:
                        print(
                            f"DIRTY: CLAUDE.md generated region for scope '{scope}' "
                            f"differs from fresh gen_rules.py output "
                            f"(run 'python tools/gen_rules.py' and commit)",
                            file=sys.stderr,
                        )
                        any_dirty = True
                    else:
                        print(f"CLEAN: CLAUDE.md region for scope '{scope}'")
            else:
                new_content, changed = _update_claude_md_region(scope, new_region)
                if changed:
                    CLAUDE_MD.write_text(new_content, encoding="utf-8")
                    print(f"gen_rules.py: updated CLAUDE.md region for scope '{scope}'")
                    generated_files.append(f"CLAUDE.md::{scope}")
                else:
                    print(f"gen_rules.py: CLAUDE.md region for scope '{scope}' already up-to-date")

        else:
            # AREA scope → render into .claude/rules/<scope>.md
            content = _render_scope(scope, scope_adrs)
            out_path = RULES_DIR / f"{scope}.md"

            if check_mode:
                # Compare to committed file
                if not out_path.exists():
                    print(
                        f"DIRTY: {out_path.relative_to(REPO_ROOT)} does not exist "
                        f"(run 'python tools/gen_rules.py' and commit)",
                        file=sys.stderr,
                    )
                    any_dirty = True
                else:
                    committed = out_path.read_text(encoding="utf-8", errors="replace")
                    if committed != content:
                        print(
                            f"DIRTY: {out_path.relative_to(REPO_ROOT)} differs from "
                            f"fresh gen_rules.py output "
                            f"(run 'python tools/gen_rules.py' and commit)",
                            file=sys.stderr,
                        )
                        any_dirty = True
                    else:
                        print(f"CLEAN: {out_path.relative_to(REPO_ROOT)}")
            else:
                out_path.write_text(content, encoding="utf-8")
                rel = out_path.relative_to(REPO_ROOT)
                print(f"gen_rules.py: wrote {rel}")
                generated_files.append(str(rel))

    if check_mode:
        # Conservation check: live count must equal the migration baseline (ADR-0073 D5).
        # If it differs, either a new ADR was added (update RULE_IDS_BASELINE) or a rule
        # was silently removed — either way the mismatch is surfaced here.
        if total_rule_ids != RULE_IDS_BASELINE:
            print(
                f"CONSERVATION FAIL: rule_id count {total_rule_ids} != baseline "
                f"{RULE_IDS_BASELINE} — update RULE_IDS_BASELINE in gen_rules.py "
                f"after confirming no rules were lost (ADR-0073 D5)",
                file=sys.stderr,
            )
            any_dirty = True
        else:
            print(
                f"CONSERVATION OK: rule_ids total {total_rule_ids} == "
                f"baseline {RULE_IDS_BASELINE}"
            )
        if not any_dirty:
            print(f"gen_rules.py: all outputs clean (rule_ids total: {total_rule_ids})")
        return 1 if any_dirty else 0

    if not generated_files:
        print("gen_rules.py: nothing to write.", file=sys.stderr)
        return 1

    return 0


if __name__ == "__main__":
    check = "--check" in sys.argv
    sys.exit(generate(check_mode=check))
